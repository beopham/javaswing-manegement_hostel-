    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String sodienthoai = jTextField1.getText();
        SimpleDateFormat fomat = new SimpleDateFormat("MMM-yyyy");
        java.util.Date date = new java.util.Date();
        String thang = fomat.format(date);
        try {
            Connection con = ketnoisql.getcon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from nhanvien where sodienthoai= '" + sodienthoai + "' and trangthai='Đang Làm Việc'");
            if (rs.next()) {
                jTextField1.setEditable(false);
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(5));
                jTextField5.setText(thang);
                jTextField6.setText("10000");

            } else {
                JOptionPane.showMessageDialog(null, "Nhân Viên Không Tồn Tại");
                clear();
            }
            chitietbang();
            ResultSet rsl = st.executeQuery("SELECT * FROM gianhanvien INNER JOIN nhanvien "
                    + "WHERE nhanvien.trangthai='Đang làm Việc' and gianhanvien.thang='" + thang + "' and gianhanvien.sodienthoai ='" + sodienthoai + " ' and nhanvien.sodienthoai ='" + sodienthoai + "'");
            if (rsl.next()) {
                jButton3.setVisible(false);
                JOptionPane.showMessageDialog(null, "Tiền Trọ Đã Được Sinh Viên Tra Trong Tháng Này");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

