        public void roomnumber() {
        int i = 0;

        try {
            Connection conn = ketnoisql.getcon();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from room where activity ='Yes' and roomstatus='Không Đặt'");

            while (rs.next()) {
                i = 1;
                jComboBox1.addItem(rs.getString(1));
            }
            if (i == 0) {
                jButton2.setVisible(false);
                JOptionPane.showMessageDialog(null, "TấT Cả Các Phòng Đã Được Đặt");
                setVisible(false);

            }

        } catch (Exception ex) {
//            JOptionPane.showMessageDialog(null, ex);
        }

    }
