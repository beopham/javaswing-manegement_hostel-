    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String sodienthoai = jTextField1.getText();
        String ten = jTextField2.getText();
        String tencha = jTextField3.getText();
        String tenme = jTextField4.getText();
        String email = jTextField5.getText();
        String dctt = jTextField6.getText();
        String tendh = jTextField7.getText();
        String masv = jTextField8.getText();
        String roomnumber = (String) jComboBox1.getSelectedItem();
        String trangthai = "Đã Sống";
        try {
            Connection conn = ketnoisql.getcon();
            PreparedStatement ps = conn.prepareStatement("insert into student values(?,?,?,?,?,?,?,?,?,?)");
            ps.setString(1, sodienthoai);
            ps.setString(2, ten);
            ps.setString(3, tencha);
            ps.setString(4, tenme);
            ps.setString(5, email);
            ps.setString(6, dctt);
            ps.setString(7, tendh);
            ps.setString(8, masv);
            ps.setString(9, roomnumber);
            ps.setString(10, trangthai);
            ps.executeUpdate();
            
            PreparedStatement psl = conn.prepareStatement("update room set roomstatus='Đã Đặt' where number=?");
            psl.setString(1, roomnumber);
            psl.executeUpdate();
            JOptionPane.showMessageDialog(null, "Bạn Cập Nhật Thành Công");
            clear();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

